program var_example_adv

integer, parameter :: count = 42
real, parameter :: temperature = 98.6
double precision, parameter :: pi_value = 3.141592653589
character, parameter :: grade = 'A'
logical, parameter :: is_valid = .true.

print*, 'count=', count
print*, 'temperature=', temperature
print*, 'pi_value=', pi_value
print*, 'grade=', grade
print*, 'is_valid=', is_valid

stop
end program var_example_adv

